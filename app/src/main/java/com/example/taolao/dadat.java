package com.example.taolao;

public class dadat {
    private String xacnhan;
    private String madon;
    private String thoigian;
    private int hinhanh;
    private String tensp;
    private String diachi;
    private int icon;
    private String gia;
    private String montienmat;

    public dadat(String xacnhan, String madon, String thoigian, int hinhanh, String tensp, String diachi, int icon, String gia, String montienmat) {
        this.xacnhan = xacnhan;
        this.madon = madon;
        this.thoigian = thoigian;
        this.hinhanh = hinhanh;
        this.tensp = tensp;
        this.diachi = diachi;
        this.icon = icon;
        this.gia = gia;
        this.montienmat = montienmat;
    }

    public String getXacnhan() {
        return xacnhan;
    }

    public void setXacnhan(String xacnhan) {
        this.xacnhan = xacnhan;
    }

    public String getMadon() {
        return madon;
    }

    public void setMadon(String madon) {
        this.madon = madon;
    }

    public String getThoigian() {
        return thoigian;
    }

    public void setThoigian(String thoigian) {
        this.thoigian = thoigian;
    }

    public int getHinhanh() {
        return hinhanh;
    }

    public void setHinhanh(int hinhanh) {
        this.hinhanh = hinhanh;
    }

    public String getTensp() {
        return tensp;
    }

    public void setTensp(String tensp) {
        this.tensp = tensp;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getGia() {
        return gia;
    }

    public void setGia(String gia) {
        this.gia = gia;
    }

    public String getMontienmat() {
        return montienmat;
    }

    public void setMontienmat(String montienmat) {
        this.montienmat = montienmat;
    }
}
